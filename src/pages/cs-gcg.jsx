import React, { useState, useEffect } from "react";
import { Helmet } from "react-helmet";

import Logo from "../components/common/logo";
import Footer from "../components/common/footer";
import NavBar from "../components/common/navBar";
import INFO from "../data/user";

import "./styles/cs-page.css"

const CSGCG = () => {

	const [stayLogo, setStayLogo] = useState(false);
	const [logoSize, setLogoSize] = useState(70);
	const [oldLogoSize, setOldLogoSize] = useState(70);

    useEffect(() => {
		window.scrollTo(0, 0);
	}, []);

	useEffect(() => {
		const handleScroll = () => {
			let scroll = Math.round(window.scrollY, 2);

			let newLogoSize = 70 - (scroll * 4) / 10;

			if (newLogoSize < oldLogoSize) {
				if (newLogoSize > 40) {
					setLogoSize(newLogoSize);
					setOldLogoSize(newLogoSize);
					setStayLogo(false);
				} else {
					setStayLogo(true);
				}
			} else {
				setLogoSize(newLogoSize);
				setStayLogo(false);
			}
		};

		window.addEventListener("scroll", handleScroll);
		return () => window.removeEventListener("scroll", handleScroll);
	}, [logoSize, oldLogoSize]);

	const logoStyle = {
		display: "flex",
		position: stayLogo ? "fixed" : "relative",
		top: stayLogo ? "3vh" : "auto",
		zIndex: 999,
		border: "2px solid var(--tertiary-color)",
		borderRadius: stayLogo ? "50%" : "none",
		boxShadow: stayLogo ? "0px 4px 10px rgba(0, 0, 0, 0.25)" : "none",
	};

	return (
		<React.Fragment>
			<Helmet>
				<title>{`Client Site: Guths Campground | ${INFO.main.title}`}</title>
			</Helmet>

			<div className="page-content">
				<NavBar active="work" />
				<div className="content-wrapper">
					<div className="cs-logo-container">
						<div className="cs-logo" style={logoStyle}>
							<Logo width={logoSize} />
						</div>
					</div>

					<div className="cs-container">
						<div className="title">
							Client Site: Guths Campground
						</div>

						<div className="subtitle cs-subtitle">
                        	Clicking the logo in the top left corner returns you to the home page.
						</div>

						<div className="subtitle cs-subtitle">
							A welcoming digital retreat designed for a local campsite, this website highlights beautiful amenities, provides an interactive photo gallery of the grounds, and incorporates strategic 
							SEO to help attract both local and out-of-town travelers.
						</div>

                        <div className="cs-main">

							<div className="cs-right-side">
								<div className="title cs-title">
									Website Features
								</div>

								<div className="subtitle cs-subtitle-desc">
									The website was made to reflect the scenic beauty and tranquil experience of staying at Guth’s Campground.
								</div>

								<div className="subtitle cs-subtitle-desc">
									The homepage features a stunning photo gallery that showcases the natural surroundings, including campsite views, hiking trails, and nearby attractions. 
									An interactive amenities page highlights key offerings such as tent sites, RV hookups, and cabin rentals, while also providing helpful information on nearby activities and points of interest for guests.
								</div>

								<div className="subtitle cs-subtitle-desc">
									To make booking easy for visitors, I integrated an online reservation form where potential campers can check availability and book their stay directly. 
									The design is mobile-friendly, ensuring a seamless experience for users browsing and booking their trips while on the go.
								</div>
							</div>

							<div className="cs-left-side">
								<div className="cs-image-container">
									<div className="cs-image-wrapper">
										<img
											src="gcg1.png"
											alt="example1"
											className="cs-image"
										/>
									</div>
								</div>
							</div>

						</div>

                        <div className="cs-main cs-main-rev">

							<div className="cs-left-side">
                                <div className="cs-image-container">
									<div className="cs-image-wrapper">
										<img
											src="gcg2.png"
											alt="example2"
											className="cs-image"
										/>
									</div>
								</div>
							</div>

							<div className="cs-right-side cs-rev">
                                <div className="title cs-title cs-title-rev">
									SEO and Optimization
								</div>

								<div className="subtitle cs-subtitle-rev">
									To help Guth’s Campground attract more visitors from both local and distant locations, I focused on implementing effective SEO strategies. The website’s content was optimized with keyword-rich descriptions, 
									location-based terms, and clear calls to action. I also applied structured data markup to ensure the campground appears in search results for relevant camping and travel queries.
								</div>

								<div className="subtitle cs-subtitle-rev">
									Performance enhancements—such as image compression, faster loading times, and optimized mobile responsiveness—help the site rank better and deliver a smooth user experience for travelers browsing or booking on any device. 
									These optimizations contribute to higher organic traffic and increased reservations.
								</div>
							</div>
						</div>
					</div>
					<div className="page-footer">
							<Footer />
					</div>
				</div>
			</div>
		</React.Fragment>
	);
};

export default CSGCG;